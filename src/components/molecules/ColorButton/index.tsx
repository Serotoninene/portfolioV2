export const ColorButton = () => {
  const { switchColors } = useColorContext();
  const [isSun, setIsSun] = useState(false);
  const pathRef = useRef<SVGPathElement>(null);

  const handleClick = () => {
    const interpolator = interpolate(
      isSun ? sunPath : moonPath,
      isSun ? moonPath : sunPath
    );
    switchColors();

    const dummyValue = { value: 0 };

    gsap.to(dummyValue, {
      value: 1,
      duration: 0.2,
      progress: 1,
      ease: Power4.easeInOut,
      onUpdate: function () {
        const path = interpolator(this.progress());
        pathRef.current?.setAttribute("d", path);
      },
    });

    setIsSun(!isSun);
  };

  return (
    <button
      onClick={handleClick}
      className="fixed bottom-3 left-3 w-7 aspect-square bg-dark md:left-5 z-10"
    >
      <svg viewBox="0 0 512 512" width="100%" height="100%">
        <path ref={pathRef} d={moonPath} fill="#fff" />
      </svg>
    </button>
  );
};
